enum Color {
  Red = 2,
  Green,
  Blue,
}
// Red、Green和Blue是枚举成员，它们的值分别是0、1和2。可以通过枚举成员的名称或值来访问它们。
let c1: Color = Color.Red;
let c2: Color = Color.Green;
let c3: Color = Color.Blue;

console.log(c1);
console.log(c2);
console.log(c3);
